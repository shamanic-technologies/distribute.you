"use client";

import { useMemo, useState } from "react";
import Link from "next/link";
import { MaturityBadge } from "@/components/maturity-badge";
import { FEATURE_GATES } from "@/lib/feature-gates";
import { RevenueChart } from "@/components/revenue/revenue-chart";
import {
  OrgConversionsTable,
  LeadConversionsTable,
  EventConversionsTable,
} from "@/components/revenue/conversions-table";
import { RevenueCostSummary } from "@/components/revenue/revenue-cost-summary";
import { Skeleton } from "@/components/skeleton";
import type { CostByName } from "@/lib/api";
import type { RevenueOverview } from "@/lib/revenue-view";

type ConversionTab = "organizations" | "leads" | "events";
const CONVERSION_TABS: { id: ConversionTab; label: string }[] = [
  { id: "organizations", label: "Organizations" },
  { id: "leads", label: "Leads" },
  { id: "events", label: "Events" },
];

function formatUsd(n: number | null): string {
  if (n === null) return "—";
  return `$${Math.round(n).toLocaleString("en-US")}`;
}

/**
 * Revenue-centric overview block — expected pipeline headline, revenue-over-time
 * chart, and the Organizations / Leads / Events conversion tabs (same set as the
 * dedicated Conversions page; each table paginates 20/page). Pure render — the
 * page owns the gate + query.
 */
export function RevenueOverviewSection({
  data,
  conversionsHref,
  costBreakdown,
  pending = false,
}: {
  data?: RevenueOverview;
  conversionsHref: string;
  costBreakdown: CostByName[];
  pending?: boolean;
}) {
  const [tab, setTab] = useState<ConversionTab>("organizations");
  // Join each event's leadId → the lead's photo (same payload) for the Events tab.
  const photoByLeadId = useMemo(
    () => new Map((data?.leads ?? []).map((l) => [l.leadId, l.photoUrl] as const)),
    [data?.leads],
  );
  // Static-shell-first: the section header, card frames, titles and the tab bar
  // render on the first paint; only the data regions (headline value, chart,
  // tables) skeleton while loading. `loading` folds `pending` (the page's
  // coordinated reveal) with a defensive `!data` guard.
  const loading = pending || !data;
  return (
    <div className="space-y-4">
      <div className="flex items-end justify-between">
        <div>
          <div className="flex items-center gap-2">
            <h2 className="font-display text-lg font-bold text-gray-800">Revenue &amp; Conversions</h2>
            <MaturityBadge level={FEATURE_GATES["conversions"].maturity} />
          </div>
          <p className="text-sm text-gray-500">Expected pipeline revenue generated by this feature.</p>
        </div>
        <Link href={conversionsHref} className="text-xs font-medium text-brand-600 hover:text-brand-700 whitespace-nowrap">
          View all conversions →
        </Link>
      </div>

      <div className="grid grid-cols-1 lg:grid-cols-3 gap-4">
        {/* Headline + chart */}
        <div className="lg:col-span-2 bg-white rounded-xl border border-gray-200 p-4 md:p-6">
          <div className="flex items-baseline justify-between mb-4">
            <h3 className="font-medium text-gray-800">Pipeline revenue over time</h3>
            <div className="text-right">
              {loading ? (
                <Skeleton className="h-8 w-28" />
              ) : (
                <p className="text-2xl font-bold text-gray-900 leading-none">
                  {formatUsd(data.totalPipelineUsd)}
                </p>
              )}
              <p className="text-[11px] text-gray-400 mt-1">expected pipeline</p>
            </div>
          </div>
          {loading ? (
            <Skeleton className="h-[260px] w-full rounded" />
          ) : (
            <RevenueChart series={data.timeSeries} />
          )}
        </div>

        {/* Cost & efficiency — replaces the old org/lead/event counters with the
            spend metrics (total / cost-of-acquisition / ROI) + top-3 sources. */}
        <RevenueCostSummary
          costBreakdown={costBreakdown}
          costEconomics={data?.costEconomics}
          pending={loading}
        />
      </div>

      {/* Conversions — Organizations / Leads / Events tabs (same set as the
          dedicated Conversions page; paginated 20/page inside each table). */}
      <div className="space-y-3">
        <div className="inline-flex items-center gap-1 bg-gray-50 border border-gray-200 rounded-lg p-1">
          {CONVERSION_TABS.map((t) => (
            <button
              key={t.id}
              type="button"
              onClick={() => setTab(t.id)}
              className={`px-3 py-1.5 text-xs font-medium rounded-md transition ${
                tab === t.id
                  ? "bg-brand-50 text-brand-700 border border-brand-200"
                  : "text-gray-500 hover:text-gray-800"
              }`}
            >
              {t.label}
            </button>
          ))}
        </div>

        {loading ? (
          <div className="bg-white rounded-xl border border-gray-200 p-4 space-y-2">
            {[0, 1, 2, 3, 4].map((i) => (
              <Skeleton key={i} className="h-9 w-full" />
            ))}
          </div>
        ) : (
          <>
            {tab === "organizations" && <OrgConversionsTable orgs={data.organizations} />}
            {tab === "leads" && <LeadConversionsTable leads={data.leads} />}
            {tab === "events" && (
              <EventConversionsTable events={data.events} photoByLeadId={photoByLeadId} />
            )}
          </>
        )}
      </div>
    </div>
  );
}
